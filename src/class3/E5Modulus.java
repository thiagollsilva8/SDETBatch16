package class3;

public class E5Modulus {
    public static void main(String[] args) {

        int number1=3;
        int number2=4;
        System.out.println(number1/number2);
        System.out.println(number1%number2);

        int noOfSlices=5;
        int noOfIndividuals=3;
        System.out.println(noOfSlices/noOfIndividuals);
        System.out.println(noOfSlices%noOfIndividuals);
    }
}
