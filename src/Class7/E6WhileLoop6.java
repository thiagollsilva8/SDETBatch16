package Class7;

public class E6WhileLoop6 {

    public static void main(String[] args) {

        // print these number 2 4 6 8 10 12 14 using a while loop

        int number = 2;

        while (number < 15) {
            System.out.println(number);

            //Below line will increase the value of the number by 2 everytime

            number += 2; // could also use number = number + 2, number ++ number ++

        }
    }
}
