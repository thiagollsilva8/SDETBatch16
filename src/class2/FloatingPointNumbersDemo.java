package class2;

public class FloatingPointNumbersDemo {
    public static void main(String[] args) {
        /*
        by default all the floating point numbers are doubles that's why we need to
        write a 'f'/'F' at the end of the float type variables.but we don't need to
        worry about it as 99% of the time we will be using a double
         */
        float grocery=20.5F; // is a smaller size data type
        System.out.println(grocery);
        double price=111111.222221563354; // is the bigger can hold double the amount of float
        System.out.println(price);
        // Break till 11:55 AM EST
    }
}
