package class7;

public class E4WhileLoop4 {

    public static void main(String[] args) {

        // write a loop that prints numbers from 1 to 5
        // Starting point is => 1
        // How much is each step => 1
        // Ending point => 5

        int number = 1;

        while (number <= 5) {

            System.out.println(number);
            number++;
        }
    }
}
